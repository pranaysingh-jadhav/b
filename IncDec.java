//Increment and Dicrement
class IncDec
{  
	public static void main(String[] args) 
	{
		int a = 56;
		int b = 34;
		--a;
		b=a-b;
		System.out.println(++b);
		a++;
		System.out.println(--a);
		System.out.println(b++);
		b++;
		System.out.println(--b);
		a--;
		System.out.println(a++);
		System.out.println(--a);
		a+=5;
		System.out.println(++b);
		System.out.println(a);
		System.out.println(b);
	}
}
